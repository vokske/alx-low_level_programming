#include "3-calc.h"

/**
 * op_add - Computes the sum of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Computes the difference of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Computes the product of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Computes the division of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Quotient of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Computes the modulus of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Modulus of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
